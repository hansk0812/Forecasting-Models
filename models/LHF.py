device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")


class Model(nn.Module):
    """
    H/patch_length LHF models
    """
    def __init__(self, configs):
        super(Model, self).__init__()
        
        assert configs.pred_len % configs.patch_length == 0, "GUI assertion!"
        self.networks = nn.ModuleList([_build_model(configs) for _ in range(configs.pred_len//configs.patch_length)])



    
    def _build_model(self, args):
        model_dict = {
            'FEDformer': FEDformer,
            'Autoformer': Autoformer,
            'Transformer': Transformer,
            'Informer': Informer,
            'Triformer': Triformer,
            'FiLM': FiLM,
            'DSTPRNN': DSTP_RNN_I,
            'DLinear': DLinear,
            'NLinear': NLinear,
            'NLinearLHF': NLinearLHF,
            'NHITS': NHITS,
            'TiDE': TiDE,
            'NBEATS': NBEATS,
            'xLSTM_TS': xLSTM_TS,
        }
        model_name = args.model.split('/')[-1]
        model = model_dict[model_name].Model(args).float()
        
        if not args.load_from_chkpt is None:
            model.load_state_dict(torch.load(args.load_from_chkpt, weights_only=True))

        if args.use_multi_gpu and args.use_gpu:
            model = nn.DataParallel(model, device_ids=args.device_ids)

        return model


    def forward(self, x_enc, x_mark_enc, x_dec, x_mark_dec,
                enc_self_mask=None, dec_self_mask=None, dec_enc_mask=None):
        # decomp init
        mean = torch.mean(x_enc, dim=1).unsqueeze(1).repeat(1, self.pred_len, 1)
        zeros = torch.zeros([x_dec.shape[0], self.pred_len, x_dec.shape[2]]).to(device)  # cuda()
        seasonal_init, trend_init = self.decomp(x_enc)
        # decoder input
        trend_init = torch.cat([trend_init[:, -self.label_len:, :], mean], dim=1)
        seasonal_init = F.pad(seasonal_init[:, -self.label_len:, :], (0, 0, 0, self.pred_len))
        # enc
        enc_out = self.enc_embedding(x_enc, x_mark_enc)
        enc_out, attns = self.encoder(enc_out, attn_mask=enc_self_mask)
        # dec
        dec_out = self.dec_embedding(seasonal_init, x_mark_dec)
        seasonal_part, trend_part = self.decoder(dec_out, enc_out, x_mask=dec_self_mask, cross_mask=dec_enc_mask,
                                                 trend=trend_init)
        # final
        dec_out = trend_part + seasonal_part
        
        if self.output_attention:
            return dec_out[:, -self.pred_len:, :], attns
        else:
            return dec_out[:, -self.pred_len:, :]  # [B, L, D]


